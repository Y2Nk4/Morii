function my_the_content_filter($content) {

    //$matches will be an array with all images
    preg_match_all("/<img[^>]+\>/i", $content, $matches);

    //remove all images form content
    foreach($matches[0] as $img) {
        //var_dump($img);
        $attachment_id_matches = null;
        preg_match('/wp-image-(\d*)/', $img, $attachment_id_matches);

        $attachment_id = $attachment_id_matches[1];
        $meta = wp_get_attachment_metadata($attachment_id);

        $meta_content = '';
        if ($meta) {
            $exif = $meta['image_meta'];
            $meta_content = '<div class="img-exif">';
            $exif_info_blocks = '';

            if (isset($exif['Make'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-make"><span class="title">Brand</span><span class="content">' . $exif['Make'] . '</span></span>';
            }
            if (isset($exif['camera']) && !empty($exif['camera'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-camera"><span class="title">Camera</span><span class="content">' . $exif['camera'] . '</span></span>';
            }
            if (isset($exif['Lens'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-lens"><span class="title">Lens</span><span class="content">' . $exif['Lens'] . '</span></span>';
            }
            if (isset($exif['aperture']) && !empty($exif['aperture'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-aperture"><span class="title">Aperture</span><span class="content">Æ’/' . $exif['aperture'] . '</span></span>';
            }
            if (isset($exif['focal_length']) && !empty($exif['aperture'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-focal_length"><span class="title">Focal Length</span><span class="content">' . $exif['focal_length'] . ' mm</span></span>';
            }
            if (isset($exif['FocalLengthIn35mmFilm'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-focal_length_35mm"><span class="title">Focal Length In 35mm Film</span><span class="content">' . $exif['FocalLengthIn35mmFilm'] . ' mm</span></span>';
            }
            if (isset($exif['ExposureTimeString'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-exposure_time"><span class="title">Exposure Time</span><span class="content">' . $exif['ExposureTimeString'] . ' s</span></span>';
            }
            if (isset($exif['ExposureProgram'])) {
                $exif_info_blocks .= '<span class="exif-tag exif-exposure_program"><span class="title">Exposure Program</span><span class="content">';

                switch($exif['ExposureProgram']){
                    default:
                    case 0:
                        $exif_info_blocks .= 'Not defined';
                        break;
                    case 1:
                        $exif_info_blocks .= 'Manual';
                        break;
                    case 2:
                        $exif_info_blocks .= 'Normal';
                        break;
                    case 3:
                        $exif_info_blocks .= 'Aperture Priority';
                        break;
                    case 4:
                        $exif_info_blocks .= 'Shutter Priority';
                        break;
                    case 5:
                        $exif_info_blocks .= 'Creative Program';
                        break;
                    case 6:
                        $exif_info_blocks .= 'Action Program';
                        break;
                    case 7:
                        $exif_info_blocks .= 'Portrait Mode';
                        break;
                    case 8:
                        $exif_info_blocks .= 'Landscape Mode';
                        break;
                }
                $exif_info_blocks .= '</span></span>';
            }
            if ($exif_info_blocks) {
                $meta_content .= $exif_info_blocks;
                $meta_content .= '</div><div class="img-exif-control"><i class="iconfont iconprompt-fill"></i></div>';
            } else {
                $meta_content = '';
            }
        }

        preg_match("/\<img.+src\=(?:\"|\')(.+?)(?:\"|\')(?:.+?)\>/", $img, $matches);

        $replacement = '<div class="post_img"><picture> <source srcset="' . $matches[1] . '" type="image/webp"> <source srcset="' . $matches[1] .'.fallback" type="image/jpeg"> ' . $img . ' </picture>' . $meta_content . '</div>';
        $content = str_replace($img, $replacement, $content);
    }

    return $content;

}

