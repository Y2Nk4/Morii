function filter_modify_metadata ($metadata, $attachment_id, $context) {
    $file_path = get_attached_file($attachment_id);
    try {
        $exif = exif_read_data($file_path);

        if ($exif) {
            if (!isset($metadata['image_meta'])) {
                $metadata['image_meta'] = [];
            }

            $metadata['image_meta']['Lens'] = $exif['UndefinedTag:0xA434'];
            $metadata['image_meta']['Make'] = $exif['Make'];
            $metadata['image_meta']['ExposureTimeString'] = $exif['ExposureTime'];
            $metadata['image_meta']['ExposureProgram'] = $exif['ExposureProgram'];
            $metadata['image_meta']['FocalLengthIn35mmFilm'] = $exif['FocalLengthIn35mmFilm'];
        }
        // exception fallback
    } catch (Exception $exception) {}
    return $metadata;
}

